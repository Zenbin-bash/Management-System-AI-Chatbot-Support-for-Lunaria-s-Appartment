private void loadTodaysConcerns() {
    String sql = "SELECT c.id, t.full_name, t.unit_number, c.concern_text, c.created_at, c.status " +
                 "FROM concerns c " +
                 "JOIN tenants_records t ON c.tenant_id = t.id " +
                 "WHERE DATE(c.created_at) = CURDATE() AND c.status = 'Pending'"; // Only today's pending concerns

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql);
         ResultSet rs = pstmt.executeQuery()) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear table before inserting new data

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("full_name"),
                rs.getString("unit_number"),
                rs.getString("concern_text"),
                rs.getTimestamp("created_at"),
                rs.getString("status")
            });
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading today's concerns.");
    }
}

private void loadResolvedConcerns() {
    String sql = "SELECT c.id, t.full_name, t.unit_number, c.concern_text, c.status, c.created_at " +
                 "FROM concerns c " +
                 "JOIN tenants_records t ON c.tenant_id = t.id " +
                 "WHERE c.status = 'Resolved' " +
                 "ORDER BY c.created_at DESC"; // Show newest resolved concerns first

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql);
         ResultSet rs = pstmt.executeQuery()) {

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("full_name"),
                rs.getString("unit_number"),
                rs.getString("concern_text"),
                rs.getString("created_at"),
                rs.getString("status")
            });
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading resolved concerns.");
    }
}


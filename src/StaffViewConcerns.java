private void loadConcerns() {
    String sql = "SELECT c.id, t.full_name, t.unit_number, c.concern_text, c.status, c.created_at " +
                 "FROM concerns c JOIN tenants_records t ON c.tenant_id = t.id";

    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement pstmt = conn.prepareStatement(sql);
         ResultSet rs = pstmt.executeQuery()) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("full_name"),
                rs.getString("unit_number"),
                rs.getString("concern_text"),
                rs.getString("created_at"),
                rs.getString("status")
            });
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading concerns.");
    }
}
